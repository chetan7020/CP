set<int>st;

int cycle=0;

void dfs(int ver){

    cout<<ver<<" ";

    if(ver==1)return;

    if(st.find(ver)!=st.end()){
        cycle=1;
        return;
    }

    st.insert(ver);

    int n=0;

    while (ver!=0) { 
        int rem= ver%10; 
        n+=(rem*rem);
        ver=ver/10; 
    } 

    dfs(n);
}

void solve(){
    int n; cin>>n;

    dfs(n);

    cout<<endl;

    if(cycle==0) cout<<"true";
    else cout<<"false";

    cout<<endl;
}

