using namespace std;

void solve(){
	int n,k; cin>>n>>k;

	// cout<<n<<k;

	vector<int>a(n);

	for(auto &i:a)cin>>i;

	srt(a);

	int ans=INT_MAX;

	int i=0,j=k-1;

	while(j<n){
		ans=min(ans, a[j]-a[i]);
		i++;
		j++;
	}

	cout<<ans;

	cout<<endl;
}

